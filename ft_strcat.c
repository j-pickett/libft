#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		pos;
	int		i;

	i = 0;
	pos = 0;
	while (s1[pos])
		pos++;
	while (s2[i])
	{
		s1[pos] = s2[i];
		pos++;
		i++;
	}
	s1[pos] = '\0';
	return (s1);
}
